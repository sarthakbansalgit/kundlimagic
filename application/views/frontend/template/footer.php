    <script>
    $('#subscriberForm').on('submit', function(e) {
        e.preventDefault();

        $.ajax({
            url: '<?= base_url("frontend/contact/insert_subscriber") ?>',
            type: 'POST',
            data: $(this).serialize(),
            success: function(response) {
                const res = JSON.parse(response);

                let alertClass = res.status === 'Success' ? 'alert-success' : 'alert-danger';
                $('#response').html('<div class="alert ' + alertClass + '" role="alert">' + res.message + '</div>');

                setTimeout(function() {
                    $('#response').fadeOut('slow', function() {
                        $(this).html('').show();
                    });
                }, 3000);
            },
            error: function(xhr, status, error) {
                $('#response').html('<div class="alert alert-danger" role="alert">' + error + '</div>');

                // Hide after 3 seconds
                setTimeout(function() {
                    $('#response').fadeOut('slow', function() {
                        $(this).html('').show();
                    });
                }, 3000);
            }
        });
    });
</script>



